--[[
Operators Overview:

Addition:
- Description: Adds two numbers.
- Syntax: x + y
- Example: 5 + 2 → 7

Subtraction:
- Description: Subtracts one number from another.
- Syntax: x - y
- Example: 5 - 2 → 3

Multiplication:
- Description: Multiplies two numbers.
- Syntax: x * y
- Example: 5 * 2 → 10

Division:
- Description: Divides one number by another.
- Syntax: x / y
- Example: 5 / 2 → 2.5

Exponential:
- Description: Raises one number to the power of another.
- Syntax: x ^ y
- Example: 5 ^ 2 → 25

Remainder (Modulo):
- Description: Gives the leftover value after division.
- Syntax: x % y
- Example: 5 % 2 → 1

Negation:
- Description: Reverses the sign of a number.
- Syntax: -x
- Example: -(3 + 2) → -5
]]

